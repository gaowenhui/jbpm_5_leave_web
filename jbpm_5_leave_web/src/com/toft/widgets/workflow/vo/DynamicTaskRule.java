package com.toft.widgets.workflow.vo;

public class DynamicTaskRule {
	private String dynamicTaskRuleName = "";
	private String dynamicTaskRuleCode = "";

	public String getDynamicTaskRuleName() {
		return dynamicTaskRuleName;
	}

	public void setDynamicTaskRuleName(String dynamicTaskRuleName) {
		this.dynamicTaskRuleName = dynamicTaskRuleName;
	}

	public String getDynamicTaskRuleCode() {
		return dynamicTaskRuleCode;
	}

	public void setDynamicTaskRuleCode(String dynamicTaskRuleCode) {
		this.dynamicTaskRuleCode = dynamicTaskRuleCode;
	}
}
