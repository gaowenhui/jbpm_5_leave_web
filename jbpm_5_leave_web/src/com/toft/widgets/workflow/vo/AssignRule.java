package com.toft.widgets.workflow.vo;

public class AssignRule {
	private String assignRuleName = "";
	private String assignRuleCode = "";
	private String assignRuleClassName="";

	public String getAssignRuleName() {
		return assignRuleName;
	}

	public void setAssignRuleName(String assignRuleName) {
		this.assignRuleName = assignRuleName;
	}

	public String getAssignRuleCode() {
		return assignRuleCode;
	}

	public void setAssignRuleCode(String assignRuleCode) {
		this.assignRuleCode = assignRuleCode;
	}

	public String getAssignRuleClassName() {
		return assignRuleClassName;
	}

	public void setAssignRuleClassName(String assignRuleClassName) {
		this.assignRuleClassName = assignRuleClassName;
	}
	
}
