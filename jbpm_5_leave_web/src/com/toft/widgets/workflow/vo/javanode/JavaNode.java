package com.toft.widgets.workflow.vo.javanode;

public class JavaNode {
	private String classpath;
	private String method;
	private String vars;
	private String continued;//continue:async,exclusire
	
	private MethodParameter[] parameters;
	private ClassField[] fields;
	public String getClasspath() {
		return classpath;
	}
	public void setClasspath(String classpath) {
		this.classpath = classpath;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	
	public String getVars() {
		return vars;
	}
	public void setVars(String vars) {
		this.vars = vars;
	}
	public String getContinued() {
		return continued;
	}
	public void setContinued(String continued) {
		this.continued = continued;
	}
	public MethodParameter[] getParameters() {
		return parameters;
	}
	public void setParameters(MethodParameter[] parameters) {
		this.parameters = parameters;
	}
	public ClassField[] getFields() {
		return fields;
	}
	public void setFields(ClassField[] fields) {
		this.fields = fields;
	}
	
	
}
