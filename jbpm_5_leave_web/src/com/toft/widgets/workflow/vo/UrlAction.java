package com.toft.widgets.workflow.vo;

public class UrlAction {
	private String urlActionName = "";
	private String urlActionCode = "";

	public String getUrlActionName() {
		return urlActionName;
	}

	public void setUrlActionName(String urlActionName) {
		this.urlActionName = urlActionName;
	}

	public String getUrlActionCode() {
		return urlActionCode;
	}

	public void setUrlActionCode(String urlActionCode) {
		this.urlActionCode = urlActionCode;
	}
}
