package com.toft.widgets.workflow.vo;

public class TimeOutAction {
	private String timeoutActionName = "";
	private String timeoutActionCode = "";

	public String getTimeoutActionName() {
		return timeoutActionName;
	}

	public void setTimeoutActionName(String timeoutActionName) {
		this.timeoutActionName = timeoutActionName;
	}

	public String getTimeoutActionCode() {
		return timeoutActionCode;
	}

	public void setTimeoutActionCode(String timeoutActionCode) {
		this.timeoutActionCode = timeoutActionCode;
	}
}
